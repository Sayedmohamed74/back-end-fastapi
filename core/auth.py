def convert_pass_hash(password):
    return password_hash.hash(password)


def compare_pass_hash(password, hash_pass):
    return password_hash.verify(password, hash_pass)


def convert_info_token(data: dict, time: int | None = None):
    to_encode = data.copy()
    if time:
        expire = datetime.now(timezone.utc) + timedelta(minutes=time)
    else:
        expire = datetime.now(timezone.utc) + timedelta(minutes=15)
    to_encode.update({"exp": expire})
    encoded_jwt = jwt.encode(to_encode, SECRET_KEY, algorithm=ALGORITHM)
    return {"token": encoded_jwt, "exp": expire.isoformat()}


def convert_token_info(token):
    credentials_exception = HTTPException(
        status_code=status.HTTP_401_UNAUTHORIZED,
        detail="Could not validate credentials",
        headers={"WWW-Authenticate": "Bearer"},
    )
    try:
        payload = jwt.decode(token, SECRET_KEY, algorithms=ALGORITHM)
        return payload
    except InvalidTokenError:
        raise credentials_exception


async def get_user(token: str = Depends(oauth2_scheme)):

    if not token:
        raise HTTPException(
            status_code=401,
            detail="Not authenticated",
            headers={"WWW-Authenticate": "Bearer"},
        )
    user = convert_token_info(token)
    return user


